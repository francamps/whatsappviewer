export default function getDateFormats () {
  return [
    /* 6/15/2009 */
    "%d/%m/%Y",
    "%-d/%-m/%Y",
    /* 15/06/2009 */
    "%m/%d/%Y",
    "%-m/%-d/%Y",
    /* 2009/06/15 */
    "%Y/%m/%d",
    "%Y/%-m/%-d",
    /* Monday, June 15, 2009 */
    "%b %d, %Y",
    "%b, %d, %Y",
    /* Monday, June 15, 2009 1:45 PM */
    "%b %d, %Y, %H:%M %p",
    "%b, %d, %Y, %H:%M %p",
    /* Monday, June 15, 2009 1:45:30 PM */
    "%b %d, %Y, %H:%M:%S %p",
    "%b, %d, %Y, %H:%M:%S %p",
    /* 6/15/2009 1:45 PM */
    "%-m/%-d/%-y, %-I:%M %p",
    /* 15/06/2009 13:45 */
    "%-m/%-d/%-y, %H:%M",
    /* 06/15/2009 13:45 */
    "%-d/%-m/%-y, %H:%M %p",
    /* 2009/6/15 13:45 */
    "%Y/%-m/%-d, %H:%M",
    /* 15/06/2009 13:45:30 */
    "%-d/%-m/%Y, %H:%M:%S",
    /* 6/15/2009 1:45:30 PM */
    "%m/%-d/%Y, %I:%M:%S %p",
    /* 2009/6/15 13:45:30 */
    "%Y/%m/%-d, %H:%M:%S",
    /* 2009-06-15T13:45:30 */
    "%Y-%-m-%-dT%H:%M:%S",
    /* 2009-06-15 13:45:30Z */
    "%Y-%-m-%-d %H:%M:%SZ",
    /* Monday, June 15, 2009 8:45:30 PM */
    "%b, %m %d, %H:%M:%S %p",
    /* */
    "%-m/%-d/%-y, %-H:%M %p",
    "%-m/%-d/%-Y, %-H:%M %p",
    "%Y-%m-%d, %H:%M:%S",
    "%b %d, %Y, %H:%M %p"
  ]
}
